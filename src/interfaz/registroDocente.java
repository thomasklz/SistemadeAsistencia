    private void btRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRegistrarActionPerformed
       
        try {
            if(txtNombre.getText().equals("") || txtApellido.getText().equals("") || txtCedula.getText().equals("") || txtEdad.getText().equals("")){
            lbError.setText("## Ingrese datos ##");
            }else{
                prof.setNombre(txtNombre.getText());
                prof.setApellido(txtApellido.getText());
                prof.setCedula((txtCedula.getText()));
                prof.setEdad(Integer.parseInt(txtEdad.getText()));
                prof.setIdDocente(Integer.toString(Docente.arrayProfesor.size()));
                prof.setIdMateria(txtidMateria.getText());
                prof.setTelefono(txtCelular.getText());
                prof.setEstadoCivil(txtEstadoCivil.getText());
               prof.IngresarPersona();
               //arrayProf.add(new Docente(prof.getIdDocente(), prof.getIdMateria(), prof.getNombre(), prof.getApellido(), prof.getCedula(), prof.getEdad(), prof.getTelefono(), prof.getEstadoCivil()));
               JOptionPane.showMessageDialog(null, "### Datos Guardados ###");
                }
            txtNombre.setText("");
            txtApellido.setText("");
            txtCedula.setText("");
            txtEdad.setText("");
            txtEstadoCivil.setText("");
            txtCelular.setText("");
            jComboBoxMaterias.setSelectedIndex(0);
            txtNombre.setEnabled(true);
            txtApellido.setEnabled(true);
            txtEdad.setEnabled(true);
            txtCelular.setEnabled(true);
            txtEstadoCivil.setEnabled(true);
            txtCedula.setEnabled(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e);
        }
        
//        txtIdDocente.setText(Integer.toString(Docente.arrayProfesor.size()));
    }//GEN-LAST:event_btRegistrarActionPerformed

