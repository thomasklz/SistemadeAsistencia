    private void btBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btBuscarMouseClicked
        try {
            String cedula = txtCedula.getText();
            boolean visualizar = false;
            if (Docente.arrayProfesor.size()==0) {
                JOptionPane.showMessageDialog(this, "Sin datos");
            }else{
                for (int i = 0; i < Docente.arrayProfesor.size(); i++) {
                    if (Docente.arrayProfesor.get(i).getCedula().equals(cedula)) {
                        txtNombre.setText(Docente.arrayProfesor.get(i).getNombre());
                        txtApellido.setText(Docente.arrayProfesor.get(i).getApellido());
                        txtEdad.setText(Integer.toString(Docente.arrayProfesor.get(i).getEdad()));
                        txtCelular.setText(Docente.arrayProfesor.get(i).getTelefono());
                        txtEstadoCivil.setText(Docente.arrayProfesor.get(i).getEstadoCivil());
                        txtNombre.setEnabled(false);
                        txtApellido.setEnabled(false);
                        txtEdad.setEnabled(false);
                        txtCelular.setEnabled(false);
                        txtEstadoCivil.setEnabled(false);
                        txtCedula.setEnabled(false);
                        visualizar=true;
                    }
                }
                if (visualizar==false) {
                    JOptionPane.showMessageDialog(this,"Usuario no encontrado");
                }
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e);
        }
    }//GEN-LAST:event_btBuscarMouseClicked

