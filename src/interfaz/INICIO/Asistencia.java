    private void btBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btBuscarMouseClicked
        try {
            String cedula = txtCedula.getText();
            valido=false;
            arrayIdMateria=new ArrayList<>();
            arrayMateriaDoc=new ArrayList<>();
            for (int i = 0; i < Docente.arrayProfesor.size(); i++) {
                if(Docente.arrayProfesor.get(i).getCedula().equals(cedula)){
                    arrayIdMateria.add(Docente.arrayProfesor.get(i).getIdMateria());
                    valido=true;
                }
            }
            if (valido==true) {
                JOptionPane.showConfirmDialog(null, "Existe");
                    for (int i = 0; i < Materia.arrayMateria.size(); i++) {
                        for (int j = 0; j < arrayIdMateria.size(); j++) {
                            if (Materia.arrayMateria.get(i).getIdMateria().equals(arrayIdMateria.get(j))) {
                                arrayMateriaDoc.add("id: "+Materia.arrayMateria.get(i).getIdMateria()+" Name: "+Materia.arrayMateria.get(i).getNombre_Materia());
                                JOptionPane.showConfirmDialog(null, "Existe");
                            }
                        }
                    }

                    for (int i = 0; i < arrayMateriaDoc.size(); i++) {
                        jComboBoxMaterias.addItem(arrayMateriaDoc.get(i));
                    }
                    txtIDmateria.setEnabled(true);
                    jButtonMostrar.setEnabled(true);
            }else{
                JOptionPane.showConfirmDialog(null, "Usuario No encontrado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e);
        }
        
    }//GEN-LAST:event_btBuscarMouseClicked

    private void jButtonMostrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonMostrarMouseClicked
        
        try {
             String materia = txtIDmateria.getText();
            int level=0;
            arrayEstudianteCed = new ArrayList<>();
            for (int i = 0; i < Materia.arrayMateria.size(); i++) {
                if (Materia.arrayMateria.get(i).getIdMateria().equals(materia)) {
                    level=Integer.parseInt(Materia.arrayMateria.get(i).getIdNivel());
                    JOptionPane.showMessageDialog(null, "materia");
                }
            }

            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            Object matriz[] = new Object[3];
            int i;
            for(i=0; i<Estudiante.arrayEstudiante.size(); i++){
                if (Estudiante.arrayEstudiante.get(i).getIdNivel()==level) {
                    arrayEstudianteCed.add(Estudiante.arrayEstudiante.get(i).getCedula());
                    matriz[0]=Estudiante.arrayEstudiante.get(i).getNombre();
                    matriz[1]=Estudiante.arrayEstudiante.get(i).getApellido();
                    matriz[2]=true;
                    JOptionPane.showMessageDialog(null, "Existe");
                    modelo.addRow(matriz);
                }
            }
            jTable1.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e);
        }
       txtCedula.setEnabled(false);
       txtIDmateria.setEnabled(false);
    }//GEN-LAST:event_jButtonMostrarMouseClicked

    private void btAsistenciaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btAsistenciaMouseClicked
        try {
//            int columna = jTable1.getColumnCount();
            int fila = jTable1.getRowCount();
            objAsisClss.setDocenteAsistencia(txtCedula.getText());
            objAsisClss.setIdAsistencia(Integer.toString(AsistenciaClass.arrayAsistencia.size()));
            objAsisClss.setFechaAsistencia(fechaDeAsistencia);
            objAsisClss.setMateriaAsistencia(txtIDmateria.getText());
            for (int i = 0; i < fila; i++) {
                objAsisEs.setIdAsistenciaE(Integer.toString(AsistenciaClass.arrayAsistencia.size()));
                objAsisEs.setNombreEstudiante((String) jTable1.getValueAt(i, 0));
                objAsisEs.setApellidoEstudiante((String) jTable1.getValueAt(i, 1));
                if ((boolean) jTable1.getValueAt(i, 2)==true) {
                    objAsisEs.setEstadoAsistencia("Asisti贸"); 
                }else{
                    objAsisEs.setEstadoAsistencia("No Asisti贸");
                }
                objAsisEs.setCedulaEstudiante(arrayEstudianteCed.get(i));
                objAsisEs.setFechaAsistencia(fechaDeAsistencia);
                objAsisEs.IngresarAsistenciaE();
            }
            System.out.println("");
            objAsisClss.IngresarAsistencia();
            btAsistencia.setEnabled(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e);
        }
        for (int i = 0; i < AsistenciaEstudiantes.arrayAsistenciaEst.size(); i++) {
            System.out.println("Nombre: "+AsistenciaEstudiantes.arrayAsistenciaEst.get(i).getNombreEstudiante()+" \tCedula: "+AsistenciaEstudiantes.arrayAsistenciaEst.get(i).getCedulaEstudiante()+" \tAsistencia: "+AsistenciaEstudiantes.arrayAsistenciaEst.get(i).getEstadoAsistencia()+" \tFecha: "+AsistenciaEstudiantes.arrayAsistenciaEst.get(i).getFechaAsistencia()+" \tid: "+AsistenciaEstudiantes.arrayAsistenciaEst.get(i).getIdAsistenciaE());
        }
    }//GEN-LAST:event_btAsistenciaMouseClicked

